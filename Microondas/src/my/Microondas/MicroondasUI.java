    private void botonStopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonStopActionPerformed
    contpausa++;
    timer.stop();
    if(contpausa==1){
        botonComida.setEnabled(true);
        botonComida.setBackground(Color.red);
        if(botonComida.isSelected()==false){
            botonComida.setText("<HTML><P>Pausado</P><BR><P>(pulse para meter comida)</P></HTML>");
        }else{
         botonComida.setText("<HTML><P>Pausado</P><BR><P>(pulse para retirar comida)</P></HTML>");   
        }
    }else if(contpausa >=2){
        tiempo.setText("");
    }
    
    }//GEN-LAST:event_botonStopActionPerformed
    
