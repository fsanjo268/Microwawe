    private void botonComidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonComidaActionPerformed
        if(botonComida.isSelected()==true){
            botonComida.setText("<HTML><P>Comida metida</P><BR><P>(pulse para retirar comida)</P></HTML>");
            botonComida.setBackground(Color.green);
        }else if(botonComida.isSelected()==false){
            botonComida.setText("<HTML><P>Microondas vacio</P><BR><P>(pulse para meter comida)</P></HTML>");
            botonComida.setBackground(Color.white);
        }
    }//GEN-LAST:event_botonComidaActionPerformed

    private void botonStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonStartActionPerformed
  if(!(tiempo.getText().equals(""))){
        timer.start();
        contpausa=0;
        botonComida.setEnabled(false);
  }
    }//GEN-LAST:event_botonStartActionPerformed
Timer timer = new Timer(1000, new ActionListener(){
        @Override
        public void actionPerformed(ActionEvent e){
            
            int timenum= Integer.parseInt(tiempo.getText())-1;
            if (timenum>=0){
                tiempo.setText(String.valueOf(timenum));
                if(botonComida.isSelected()){
                    botonComida.setText("Calentando comida");
                }else{
                    botonComida.setText("Calentando nada dentro");
                }
            }else{
                timer.stop();
                botonComida.setEnabled(true);
                if(botonComida.isSelected()){
                    botonComida.setText("<HTML><P>Comida lista</P><BR><P>(pulse para retirar comida)</P></HTML>");
                }else{
                   botonComida.setText("<HTML><P>Comida lista</P><BR><P>(pulse para meter comida)</P></HTML>"); 
                }
                
                Toolkit.getDefaultToolkit().beep();
                botonComida.setBackground(Color.green);
            }
        }
    });
